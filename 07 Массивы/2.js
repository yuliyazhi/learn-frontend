// Дан массив `a`. Выведете минимальный и максимальный элементы массива.

// Пример: `let a = [5, 2, -1, 9];`

let a = [5, 2, -1, 9];;
let min = a[0];
let max = a[0];

for (let i of a) {
    if (i < min) {
        min = i;
    }

    if (i > max) {
        max = i;

    }

}
console.log("Минимальный: " + min + ", Максимальный: " + max)