// Метод forEach() выполняет указанную функцию один раз для каждого элемента в массиве.
// константу создавать не нужно
// ничего не возвращает, для кажд элемента производит какую - то колбэк функцию
// Интерактивный пример

const array1 = ['a', 'b', 'c'];

array1.forEach((element) => console.log(element));

// Expected output: "a"
// Expected output: "b"
